
# *TYPE() Funtion
# type() function is used to find the data type of a given variable in python.
a = 31
print(type(a))  # class <int>

b = "31"
print(type(b))  # class <str>

print("\n")

# *TYPECASTING:
# The process of converting one data type to another is called Typecasting.
# A number can be converted into string and vice versa (if possible).
# There are many functions to convert one data type into another.
print(type(str(28)))  # integer to string conversion
print(type(int("25")))  # string to integer conversion
print(type(float(32)))  # integer to float conversion
