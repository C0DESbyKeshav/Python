# Accessing list or tuple or string is much easier using loops.

print("Accessing List:")
l = [32, 5, 0, -39, 5, 7, 300]
for i in l:
    print(i)

print("\nAccessing Tuple:")
t = (32, 5, 0, -39, 5, 7, 300)
for i in t:
    print(i)

print("\nAccessing String:")
s = "Keshav Mandal"
for i in s:
    print(i)
